int	ft_atoi(char *str)
{
	int	i;
	int	j;
	int	k;

	i = 0;
	j = 0;
	k = 0;
	while ((str[i] == '\t') || (str[i] == '\f') || (str[i] == '\v')
			|| (str[i] == '\n') || (str[i] == '\r') || (str[i] == ' '))
		i++;
	if (str[i] == '-')
		j = 1;
	if ((str[i] == '+') || (str[i] == '-'))
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		k *= 10;
		k = k + ((int)str[i] - 48);
		i++;
	}
	if (j == 1)
		return (-k);
	else
		return (k);
}
